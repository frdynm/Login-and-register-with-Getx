void main() {
  //final dan const == isi dari variable itu gabisa di ubah sama sekali
  //bedanya kalo pake final bisa di kosongin lalu di isi
  final String suami;
  suami = "ferdy";
  print(suami);
  // jadi diatas itu cara pake final nya -> jadi si variable nya bisa di buat dulu tanpa isi

  const String istri = "ajeng";
  print(istri);
  //nah kalo misalnya pake const itu si variablenya harus di isi langsung gabisa di kosongin dulu
}
